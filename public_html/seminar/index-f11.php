<li><b>September 30, 11:00am, room KE 403</b><br>
<u>Speaker</u>: Luis A. Bazan, Department of Management Science, University of Miami<br><br>
<u>Title</u>: Investigation Between Faculty's Classroom Practices, Student's Perceptions of Effectiveness, and Pedagogical Research<br><br>
<u>Abstract</u>:
This applied dissertation was designed to improve learning in introductory business statistics courses at School of Business of the University of Miami. Following the new strategic plan that called for improvement of learning experience, the Management Science (MAS) Department departed from the traditional approach of statistics as a discipline that relied on numerical accuracy with artificial data sets and stepwise formulaic pedagogy. Instead, it presented statistics as an interdisciplinary approach that would permit students to use statistics to answer real world questions and to communicate statistical results effectively. In addition, some faculty members were not taking advantage of technology and were not familiar with the new trends in statistical education so that they could improve the learning experience of their students.
The writer compared the degree of actual faculty use of different teaching activities, the students' perceptions of the most effective teaching activities, and the effective teaching activities that modern, statistical, educational literature identified and recommended. The outcomes of this study allowed the writer to make recommendations to the faculty and administrators about the use of the most effective classroom techniques to improve the learning experience of the students. Faculty should be encouraged to increase their use of effective teaching activities and to follow the recommendations of new trends in statistical teaching research as an additional source for improving course lessons in the near future.
<br><br>

<li><b>September 22, 2:00pm, room KE 403</b><br>
<u>Speaker</u>: Atul Bhandari, SmartOps Corporation<br><br>
<u>Title</u>: At the Crossroads of Optimization and Analytics<br><br>
<u>Abstract</u>:
As global markets continue to tighten, companies are forced to reexamine their approach to supply chain management and planning.  Holding on to expensive inventory, paying higher prices for expedited deliveries, or missing orders altogether are no longer options as companies battle for market share. While enterprise resource planning (ERP) systems may manage supply chain processes under perfect conditions, the reality is that exceptions are more the norm when it comes to real world global supply chains.  The ability to quantify and navigate uncertainty and complexity is a must for any supply chain planning solution. 
Using my experience from collaborating with supply chain practitioners from market-leading companies, I will highlight areas in which SmartOps has led innovation such as the ability to quantify forecast accuracy and bias, protect against supply variability, optimize product margins, optimize tradeoff between production changeover and holding costs, synchronize inventory targets across multiple stages of supply chains, enabling order based service level policies and increase visibility of what drives inventory and service level performance. This talk will feature the role operations research plays in fact-based, optimization-led, analytics-proofed decision making for supply chain planning.
<br><br>

