<li><b>December 3rd, 11:00 a.m., room KE 403</b><br>
<u>Speaker</u>: Victor Jose, Fuqua School of Business, Duke University<br><br>
<u>Title</u>: Improving Forecast Verification Through the Incorporation of Baseline Distributions<br><br>
<u>Abstract</u>: 
The need for a quantitative measure of information - or more generally, a practical measure of the distance from one distribution to some other distribution - arises in many fields such as forecasting (where scoring rules are used to provide incentives for probability estimation), signal processing (where information gain is measured in physical units of relative entropy), decision analysis (where new information can lead to improved decisions), and finance (where investors optimize portfolios based on their private information and risk preferences).  In this talk, we generalize the two most commonly used parametric families of scoring rules and demonstrate their relation to well-known generalized entropies and utility functions, shedding new light on the characteristics of alternative scoring rules as well as duality relationships between utility maximization and entropy minimization. The novel feature of these scoring rules is that they allow probability forecasts to be evaluated relative to a pre-specified, not-necessarily-uniform baseline distribution. 
<br><br>

<li><b>October 24th, 11:00 a.m., room KE 403</b><br>
<u>Speaker</u>: Tallys Yunes, Department of Management Science, University of Miami<br><br>
<u>Title</u>: Building Efficient Product Portfolios at John Deere & Co. and at another Fortune 500 Industrial Manufacturer<br><br>
<u>Abstract</u>:
John Deere & Co. (Deere), one of the world's leading producers of 
machinery, manufactures products comprised of   
various features, within which a customer may select one of a number
of possible options.
On any given Deere product line, there may be 
tens of thousands of  combinations of options (configurations) that are 
feasible.  Maintaining such a large number 
of configurations inflates overhead costs; consequently, Deere wishes
to reduce the number of configurations from
their product lines without upsetting customers or sacrificing profits.  
We provide an explanation of the 
marketing and operational methodology used, and
tools built, to evaluate the potential for streamlining
two product lines at Deere.
For the two studied product lines, 
an increase in profit from 8 to 18% has been identified, 
possible through reducing the number of configurations by 20 to 50% 
from present levels, while maintaining the current high customer 
service levels.  Based on our analysis and the insights it generated, Deere 
recently implemented a new product line strategy.  We briefly detail
this strategy, which has thus far increased profits by tens of
millions of dollars.
As a follow-up to this project, we worked with another Fortune 500
industrial manufacturer whose challenges have some commonality with
Deere's. We show how their problem differs from the problem solved at
John Deere and how we adapted our methodology to suit their needs.
Our efforts helped them define a new bundling and price-sheet strategy
for 2008/09.
This is joint work with Alan Scheller-Wolf, Masha Shunko, Valerie Tardif,
Sridhar Tayur, and Natalya Trapp.
