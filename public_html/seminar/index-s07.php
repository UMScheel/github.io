<h2>Past Talks</h2>
<p>
<ul>
<li><b>May 4th</b><br>
<u>Speaker</u>: Tuba Aktaran-Kalayci, Department of Industrial and Systems Engineering, The State University of New York at Buffalo<br><br>
<u>Title</u>: Results in Analysis of Steady State Simulation and Queueing Models<br><br>
<u>Abstract</u>: In this talk, we present results from two separate research topics.
The first problem is estimating the variance parameter of a stationary stochastic process such as that from a steady-state production process. This work studies a new estimator based on a linear combination of a number standardized time series estimators. We establish the theoretical properties of the linear combination estimators and show that they have better performance than their predecessors. We illustrate our findings with analytical and Monte Carlo examples.
Secondly, we consider the problem of maximizing the long-run average reward in a facility with dynamic pricing. We investigate sensitivity of optimal pricing policies for the facility, which is modeled as an M/M/s/K queueing system. Arrival process to the facility is a decreasing function of the price. We prove some structural results on the optimal pricing policies when the parameters in the facility change. We illustrate how these structural results simplify the required computational procedures to find the optimal policy.<br><br>

<p>

<li><b>April 5th</b><br>
<u>Speaker</u>: Daniel Bienstock, Department of Industrial Engineering and Operations Researhc, Columbia University<br><br>
<u>Title</u>: Experiments in Robust Portfolio Optimization<br><br>
<u>Abstract</u>: Robust optimization is a relatively recent discipline for performing optimization under data uncertainty. Rather than specifying an explicit distribution for uncertain parameters, robust optimization instead takes an agnostic approach and assumes that a constrained "adversary" chooses uncertain data after the "decision maker" has chosen his or her actions. In this fashion minimization problems become min-max problems; the benefit of this agnostic approach is that it reveals hidden weaknesses in nominally optimal decisions. We describe continuing experiments with portfolio optimization problems, using realistic models of return uncertainties.<br><br>

<p>

<li><b>March 9th</b><br>
<u>Speaker</u>: Mark Van Oyen, Department of Industrial and Operations Engineering, University of Michigan<br><br>
<u>Title</u>: Operational Flexibility: Methodology and Insights with Emphasis
on Cross-training in Call Centers<br><br>
<u>Abstract</u>: Many organizations are working hard to increase the level of flexibility
in their operations.  This is due in part to changes in technology,
heightened consumer expectations for niche products, decreased vertical
integration within the supply chain, and intense global competition.
One of the most powerful mechanisms for achieving operational
flexibility is to provide sources of production capacity (e.g., workers,
machines, workstations, and plants) with multiple capabilities or
functions (e.g., cross-training workers, installing flexible machines,
redesigning workstations and tooling to support flexible production, and
building flexible or mixed-model plants).  Our objective is to develop a
computationally lightweight decision-support methodology to identify
which of alternative production system designs is the more flexible.
We provide evidence that the structure of server capabilities has a
large impact on system performance; moreover, the relative flexibility
of a design can be approximated in a useful way using a deterministic
network model.  We use linear programs that are simpler and faster than
simulation to compute deterministic flexibility indices. Our structural
flexibility method (SFM) uses a standard maxflow algorithm to compute a
scalar index of flexibility for a highly dynamic environment with only
rough capacity information.  Our average path length index (APL) tackles
the same problem via a small world network modeling approach.  The
capability flexibility index (CFI) uses detailed mean capacity and
demand data.   We tested our methods using closed serial and open
parallel queueing networks in stationary and non-stationary
environments. We applied the CFI to the problem of call center shift
scheduling to illustrate the value of such a methodology in minimizing
average waiting time by creating more flexible rosters. <br><br>

