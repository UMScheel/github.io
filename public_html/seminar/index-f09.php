<p>
<ul>
<li><b>October 16, 11am, room 217 Jenkins bldg. (dean's conference room)</b><br>
<u>Speaker</u>: Harihara Natarajan, Department of Management, University of Miami<br><br>
<u>Title</u>: Joint Dynamic Pricing of Multiple Perishable Products Under Consumer Choice<br><br>
<u>Abstract</u>:
Motivated by revenue management initiatives in industry, we consider a dynamic pricing problem facing a firm that sells given initial inventories of multiple substitutable and perishable products over a finite selling horizon.  Since the products are substitutable, individual product demands are linked through consumer choice processes.  Hence, the seller must formulate a joint dynamic pricing strategy while explicitly incorporating consumer behavior.  For a general model of consumer choice, we model this multi-product dynamic pricing problem as a stochastic dynamic program and characterize its optimal prices.  In addition, since consumer behavior depends on the nature of product differentiation, we introduce a linear random utility framework that captures the cases of vertical and horizontal product differentiation.  When products are vertically differentiated, our results show monotonicity properties (with respect to quality, inventory, and time) of the optimal prices and reveal that the optimal price of a product depends on higher quality product inventories only through their aggregate inventory rather than individual availabilities.  Further, this aggregate inventory solely determines the product's markup over an adjacent lower quality product in the assortment.  We exploit these properties to develop a polynomial-time exact algorithm for determining the optimal prices.  When products are horizontally differentiated, we find that analogous monotonicity properties do not hold.  Additionally, we find that individual, rather than aggregate, product inventory availability drives pricing in this case.
This is joint work with Yalcin Akcay (Koc University, Istanbul, Turkey) and Susan Xu (Penn State University, State College, PA).
<br><br>


<li><b>September 11, 11am, room KE 403</b><br>
<u>Speaker</u>: Tallys Yunes, Department of Management Science, University of Miami<br><br>
<u>Title</u>: Valid Inequalities for a Piecewise-Linear Objective with Knapsack and Cardinality Constraints<br><br>
<u>Abstract</u>:
We study the problem of maximizing a nonlinear function that can be approximated by a sum of separable continuous piecewise linear functions. The variables are constrained by a knapsack constraint and by a cardinality constraint stating that at most C of them can be positive. Cardinality constraints have applications in many fields, including finance and bio-informatics. We present basic polyhedral results and propose a new family of valid inequalities for this problem based on the lifting of cover inequalities. This is joint work with Ismael de Farias.
