<h2>Interesses de Pesquisa</h2>
<p>
Interesso-me principalmente pela resolu&ccedil;&atilde;o de problemas
de otimiza&ccedil;&atilde;o que se originam de aplica&ccedil;&otilde;es pr&aacute;ticas
diversas, tais como: escalonamento (esportes, pessoas, e m&aacute;quinas),
simplifica&ccedil;&atilde;o de linhas de produ&ccedil;&atilde;o,
geometria computacional,
