<li><b>September 28th</b><br>
<u>Speaker</u>: Nan Kong, Weldon School of Biomedical Engineering, Purdue University<br><br>
<u>Title</u>: Issues and Challenges in Optimal Design for Cadaveric Liver Sharing<br><br>
<u>Abstract</u>: Cadaveric liver transplantation is the only viable therapeutic option for end-stage liver disease patients
who have no living donors. However, this type of transplantation is hindered in the United States by donor
scarcity and organ viability decay. Given these difficulties, the current U.S. liver transplantation and allocation policy attempts to balance allocation likelihood and geographic proximity by allocating cadaveric livers
hierarchically. 
In this talk, we mainly consider the problem of maximizing the intra-regional transplant efficiency
through the design of organ harvesting regions. We formulate the problem as a partitioning problem that
clusters organ procurement organizations (OPOs) into regions. We refine the estimate of viability-adjusted
transplant quantity to capture the tradeoff between large and small regions. Our partitioning formulation
includes too many potential regions to handle explicitly, which leads us to a branch-and-price approach. The
pricing problem is an NP-hard nonlinear 0-1 program for which we provide a linear reformulation and design
a decomposition heuristic to generate promising columns quickly. Finally we present computational studies
that show the benefit of region design and the efficacy of our branch-and-price approach. Our test instances
are generated based on recent clinical data. Almost all instances can be solved within a reasonable amount
of time and the resulting optimal designs indicate an average viability-adjusted increase of nearly 9% over
the current regional configuration. In the remainder of the talk, we present issues and challenges in network 
configuration 
and policy design for the U.S. organ transplantation and allocation system.<br><br>
<u>About the Speaker</u>: Nan Kong is an Assistant Professor in the Weldon School of Biomedical Engineering at Purdue University. He joined Purdue BME in August 2007. Prior to the appointment at Purdue, he was an Assistant Professor in the Department of Industrial and Management Systems Engineering at the University of South Florida, Tampa, FL. He received his Ph.D. in 2006 from the Department of Industrial Engineering at the University of Pittsburgh. His research interests include health systems engineering, data-driven modeling in medical decision making, stochastic optimization, and computational discrete optimization. He received an Honorable Mention for the Dantzig Doctoral Dissertation Award given by the Institute for Operations Research and the Management Sciences (INFORMS) and the 1st Place for the Pritzker Doctoral Dissertation Award given by the Institute of Industrial Engineers (IIE).<br><br>
