<li><b>February 13th, 11:00am, room KE 403.</b><br>
<u>Speaker</u>: Natalia Yankovic, Columbia University<br><br>
<u>Title</u>: Nurse Staffing: Patients or Patience?<br><br>
<u>Abstract</u>:
Nurse staffing in hospitals is traditionally based on target ratios of patients to nurses. However, varying needs for care based on age, diagnosis, case severity, and medical complexity, as well as emerging evidence relating nursing levels to quality of care have led to the search for better staffing models. This talk will describe a newly developed queuing model for nurse staffing and an on-going hospital-based project to implement it to assure timely nursing responses to patients' needs.
