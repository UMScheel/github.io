<li><b>February 24</b><br>
<u>Speaker</u>: Eric Johnson, Tuck School of Business, Dartmouth College<br><br>
<u>Title</u>: Quality Risk Ratings in Global Supply Chains<br><br>
<u>Abstract</u>:
Extended enterprises face many challenges in managing the product quality of their suppliers. Consequently characterizing the quality risk posed by value-chain partners has become increasingly important. There have been several recent efforts to develop frameworks for rating the quality risk posed by suppliers. We develop an analytical model to examine the impact of such quality ratings on suppliers, manufacturers, and social welfare. While it might seem that quality ratings would benefit high-quality suppliers and hurt low-quality suppliers, we show that this is not always the case. We find that such quality ratings can hurt both types of suppliers or benefit both, depending on the market conditions. We also find that quality ratings do not always benefit the most demanding manufacturers who desire high-quality suppliers. Yet, in all cases, we find that social welfare is improved when quality ratings are adopted. This result suggests that quality risk ratings should be encouraged through public policy initiatives.
