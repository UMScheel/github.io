<li><b>September 24th</b><br>
<u>Speaker</u>: Maria de Lourdes Hulett, Department of Management Science, University of Miami<br><br>
<u>Title</u>:
Analytical Approximations to Predict the Performance Measures of Manufacturing Systems with Job Failures and Parallel Processing
<br><br>
<u>Abstract</u>:
Parallel processing is prevalent in many manufacturing and service systems. Many
manufactured products are built and assembled from several components fabricated
in parallel lines. An example of this manufacturing system configuration is observed
at a manufacturing facility equipped to assemble and test web servers. Characteristics
of a typical web server assembly line are: multiple products, job circulation,
and paralleling processing. The primary objective of this research was to develop
analytical approximations to predict performance measures of manufacturing systems
with job failures and parallel processing. The analytical formulations extend
previous queueing models used in assembly manufacturing systems in that they can
hadle serial and different configurations of paralleling processing with multiple product
classes, and job circulation due to random part failures. In addition, appropriate
correction terms via regression analysis were added to the approximations in order
to minimize the gap in the error between the analytical approximation and the simulation
models. Markovian and general type manufacturing systems, with multiple
product classes, job circulation due to failures, and fork and join systems to model
parallel processing were studied. In the Markovian and general case, the approximations
without correction terms performed quite well for one and two product problem
instances. However, it was observed that the flow time error increased as the number
of products and net traffic intensity increased. Therefore, correction terms for
single and fork-join stations were developed via regression analysis to deal with more
than two products. The numerical comparisons showed that the approximations perform
remarkably well when the corrections factors were used in the approximations.
In general, the average flow time error was reduced from 38.19% to 5.59% in the
Markovian case, and from 26.39% to 7.23% in the general case. All the equations
stated in the analytical formulations were implemented as a set of Matlab scripts.
By using this set, operations managers of web server assembly lines, manufacturing
or other service systems with similar characteristics can estimate different system
performance measures, and make judicious decisions - especially setting delivery due
dates, capacity planning, bottleneck mitigation, among others.
