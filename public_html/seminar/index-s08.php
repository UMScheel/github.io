<li><b>January 18th, 11:00am, room KE 403</b><br>
<u>Speaker</u>: Robert Lund, Department of Mathematical Sciences, Clemson University<br><br>
<u>Title</u>: Periodic Time Series<br><br>
<u>Abstract</u>:
This talk overviews modeling and inference procedures for time series
data
with periodic means and autocovariances.  Such series arise in
hydrology,
meteorology, astronomy, economics, and ecology.  The class of periodic
autoregressive moving-average (PARMA) models is introduced to describe
series with second-order periodicities.  PARMA models are compared and
contrasted to seasonal autoregressive moving-average models.  Testing
for
the presence of second order periodicities, asymptotic properties of
PARMA
parameter estimators, and parsimonious PARMA modeling are central issues
of the talk.  An application to United States temperature trends is
given.<br><br>
