<li><b>October 26th</b><br>
<u>Speaker</u>: Amitabh Sinha, Ross School of Business, University of Michigan<br><br>
<u>Title</u>: Integrated Optimization of Procurement, Processing and Trade of Commodities<br><br>
<u>Abstract</u>: We consider an integrated optimization problem for a firm involved in procurement, processing and trading of commodities. We first derive optimal policies for a risk-neutral firm, when the processed commodity(ies) are sold using futures instruments. We find that the optimal procurement quantity is governed by a threshold policy, where the threshold is independent of the starting inventory level, and it is optimal to postpone all processing till the last possible period. We extend the model to include risk-averse firms, using a Value-at-Risk constraint on the total expected profits. We show that the optimal procurement quantity for a risk-averse firm is never greater than that for a risk-neutral firm and a risk-averse firm may find it optimal to process and sell the output commodity in earlier periods. We conduct numerical studies to quantify the benefit from integrated decision making and the impact of risk-aversion on expected profits. (For a copy of the paper contact Tallys Yunes.)<br><br>
<u>About the Speaker</u>: Amitabh Sinha's research interests include logistic network design; social networks and their impact on business innovation; decision making under uncertainty; supply chains; location theory; and product placement. Among other projects, he is currently working on developing models of interactions among employees in corporations, and exploring how the inter-employee social network impacts information flow and innovation in an organization. He is also exploring why, in a competitive market, the first entrant may not always succeed: Along with a colleague, he has found that contrary to popular belief, the market structure could be such that the second entrant always captures greater market-share than the first entrant.<br><br>

