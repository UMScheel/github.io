<li><b>April 30, 11am, room KE 403</b><br>
<u>Speaker</u>: Woonghee Tim Huh, Department of IEOR, Columbia University<br><br>
<u>Title</u>: Data-Driven Inventory Management<br><br>
<u>Abstract</u>:
We consider inventory planning problems where the distribution of demand
distribution is not available a priori, and lost sales are not observable.
We take a non-parametric approach, and propose adaptive algorithms that
generate a sequence of ordering decisions over time, where the decision in
each period depends only on historical sales data.  We show that our
adaptive algorithms converge to the optimal solution, and establish the
convergence rate.
<br><br>

<u>About the Speaker</u>: Woonghee Tim Huh is an associate professor in the Department of Industrial
Engineering and Operations Research at Columbia University.  His current
research interests include developing data-driven adaptive policies,
proving structural results in the inventory theory, and applications of
operations management to water sustainability.  He has received Columbia
Engineering School's Distinguished Teaching Award and the Diversity
Teaching Award.  He received a B.A. in sociology, B.Math in computer
science and M.Math in combinatorics &amp; optimization from the University of
Waterloo, and holds an M.Sc. and a Ph.D. in Operations Research from
Cornell University.
<br><br>

<li><b>March 26</b><br>
<u>Speaker</u>: Mariana Olvera-Cravioto, Department of Industrial Engineering and Operations Research, Columbia University<br><br>
<u>Title</u>: Inventory Control with Recorded and Unrecorded Demands<br><br>
<u>Abstract</u>:
We consider a single-product periodic-review inventory system with lost sales. In each period, we assume that the system faces two types of uncertain demand, "recorded" and "unrecorded"; the recorded demand refers to the paying customers whose transactions are updated in the system whereas the unrecorded demand refers to a reduction of inventory that is not updated in the system, either due to information system incapability (unrecorded sales) or pilferage (loss). Due to the presence of unrecorded demand, the actual and the recorded inventory levels may disagree, but the managerial decisions, such as inventory counting and replenishment, must be made solely on the recorded inventory level. We introduce a model whose optimal counting/ordering policy can be computed by solving a dynamic program with a two-dimensional state. Furthermore, under some assumptions about the demand distributions, we show that the optimal policy is of threshold-type, in particular, it can be characterized by only two parameters.
This is joint work with Tim Huh and Ozalp Ozer.
<br><br>

<li><b>February 20</b><br>
<u>Speaker</u>: Mahesh Nagarajan, Sauder School of Business, University of British Columbia<br><br>
<u>Title</u>: Dynamic Stable Alliance Structures in Supply Chains<br><br>
<u>Abstract</u>: 
Alliance formation is a commonly observed phenomenon in several supply chains. Examples can be seen in almost every industry sector. In this talk we analyze the structure of stable alliance structures in two different settings: (i) coalitions among players selling substitutable goods in a competitive market & (ii) supplier coalitions among component suppliers in a decentralized assembly system. Contrary to much of the existing literature on coalition formation, in our analysis, we assume that players contemplating alliance formation are farsighted i.e. are capable of factoring in trade-offs associated with future defections and counter defections from any status quo position. We use two notions of dynamic stability proposed in the literature, the Largest Consistent Set (LCS) and the EPCF (Equilibrium Process of Coalition Formation). In this talk, we discuss the effect of market factors such as price elasticity, degree of competition, demand variability, number of players etc. on the stable alliance structures. Further, we discuss computational difficulties associated with computing the stable outcomes and the first set of results on the complexity of this task and approximation algorithms to calculate the set of stable outcomes. We will also discuss some new results of asymptotic analysis on these games.
</ul>

