<h2>Past Talks</h2>
<p>
<ul>
<li><b>April 29</b><br>
<u>Speaker</u>: Tallys Yunes, Department of Management Science, University of Miami<br><br>
<u>Title</u>: Valid Inequalities for the Cumulative Constraint and the Cumulative
Job Shop Scheduling Problem<br><br>
<u>Abstract</u>:
In Constraint Programming, the cumulative global constraint is used to
describe scheduling problems in which a machine can process more than one
job at a time. We study the convex hull of feasible solutions to the
cumulative constraint when (i) jobs are identical, and (ii) jobs differ in
their resource utilization rates. In the first case, we revisit some of the
previous work found in the literature; in the second case, we propose a new
family of facet-defining inequalities. We also present computational results
obtained with a branch-and-cut algorithm for cumulative scheduling, including
a cumulative version of the well-known job shop scheduling problem.
This is joint work with Dimitrios Magos and Ioannis Mourtos.
<br><br>

<li><b>March 11</b><br>
<u>Speaker</u>: Christopher Tang, Anderson School of Management, University of California, Los Angeles<br><br>
<u>Title</u>: Managing Opportunistic Supplier Product Adulteration: Deferred Payments, Inspection, and Combined Mechanisms<br><br>
<u>Abstract</u>:
Recent cases of product adulteration by foreign suppliers have compelled many manufacturers to re-think approaches to deterring suppliers from cutting corners, especially when manufacturers cannot fully monitor and control the suppliers' actions. Recognizing that process certification programs, such as ISO9000, do not guarantee unadulterated products and that product liability and product warranty with foreign suppliers are rarely enforceable, manufacturers turn to mechanisms that make payments to the suppliers contingent on no defects discovery. In this paper we study: (a) the deferred payment mechanism --- the buyer pays the supplier after the deferred payment period only if no adulteration has been discovered by the customers; (b) the inspection mechanism --- the buyer pays the supplier immediately, contingent on product passing the inspection; and (c) the combined mechanism --- a combination of the deferred payment and inspection mechanisms. We find the optimal contracts for each mechanism, and describe the Nash equilibria of inspection sub-games for the inspection and the combined mechanisms. The inspection mechanism cannot completely deter the suppliers from product adulteration, while the deferred payment mechanism can. Surprisingly, the combined mechanism is redundant: either the inspection or the deferred payment mechanisms perform just as well. Finally, the four factors that determine the dominance of deferred payment mechanism over the inspection mechanism are: (a) the inspection cost relative to inspection accuracy, (b) the buyer's liability for adulterated products, (c) the difference in financing rates for the buyer and the supplier relative to the defects discovery rate by customers, and (d) the difference in production costs for adulterated and unadulterated product.
Joint work with Volodymyr Babich, Georgetown University
<br><br>
<u>Speaker's Short Bio</u>:
Christopher Tang, a member of the faculty since 1985, served as Senior Associate Dean at the UCLA Anderson School from 1998 to 2002, and as Dean of the Business School at the National University of Singapore from 2002 to 2004.
 His research work deals with complex issues arising from production planning, supply chain management and the marketing/manufacturing interface. He has co-edited 4 books and published over 80 articles in journals such as Management Science, Operations Research, Manufacturing and Service Operations Management, Journal of Marketing Research, Production and Operations Management, and Sloan Management Review. Moreover, he has served on editorial boards for over 15 journals, including Management Science, Operations Research, Manufacturing, Service and Operations Management, and Production and Operations Management.
 In addition to his academic teaching experience at Hong Kong University of Science and Technology, National University of Singapore, Stanford University, and UCLA, he has
 various executive programs throughout Asia, Europe and Americas. He has advised over 20 multi-national firms such as Amgen, Hewlett Packard Company (world-wide locations), Honda America, Nestl&eacute; SA, IBM (world-wide locations).
 Chris received his BSc (First Class Honors) from King's College, University of London, M.A., MPhil and PhD from Yale University.
