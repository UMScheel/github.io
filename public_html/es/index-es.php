<h2>Intereses de Investigaci&oacute;n</h2>
<p>
Me intereso principalmente por la resoluci&oacute;n de problemas
de optimizaci&oacute;n que se originan de aplicaciones pr&aacute;cticas
diversas, tales como: escalonamiento (deportes, personas, y m&aacute;quinas),
simplificaci&oacute;n de l&iacute;neas de producci&oacute;n,
geometr&iacute;a computacional,
transporte, localizaci&oacute;n,
y gerencia de operaciones en general.
Para atacar problemas de este tipo, utilizo herramientas anal&iacute;ticas
de <A HREF="http://www.scienceofbetter.org">investigaci&oacute;n
operativa</A> (IO) y <A
HREF="http://kti.ms.mff.cuni.cz/~bartak/constraints/">programaci&oacute;n con restricciones</A> (PR). 
Estas herramientas son esencialmente modelos matem&aacute;ticos que contienen
una representaci&oacute;n sencilla (sino &uacute;til) del problema real que puede ser resuelta por un programa de computadora.
<P>
En algunos casos, t&eacute;cnicas tradicionalmente utilizadas en la resoluci&oacute;n de modelos de IO y PR,
cuando aplicadas aisladamente, no son capaces de resolver los modelos matem&aacute;ticos
mencionados por encima.
As&iacute; siendo, parte de mis esfuerzos de investigaci&oacute;n est&aacute;n encaminados hasta el
desarrollo de la teor&iacute;a y implementaci&oacute;n de algoritmos de optimizaci&oacute;n
que combinan (integran) m&uacute;ltiplas t&eacute;cnicas.
La edici&oacute;n de Spring 2006 de la <A HREF="http://www.tepper.cmu.edu/news-multimedia/tepper-magazine/index.aspx">Tepper Magazine</A>
tiene
<a href="../tepper-magazine-spring06-simpl.pdf">un art&iacute;culo sobre este asunto</a>
titulado "<I>Two disciplines join to optimize solutions to enterprise-wide problems</I>".
