<li><b>April 11th, 11:00am, room KE 403.</b><br>
<u>Speaker</u>: Sujin Kim, School of Industrial Engineering, Purdue University<br><br>
<u>Title</u>: Adaptive Control Variate Methods in Monte Carlo Simulation<br><br>
<u>Abstract</u>:
In this talk we discuss recent results in adaptive control variate methods.
Adaptive Monte Carlo methods are Monte Carlo simulation techniques that aim at improving performance of simulation experiments by adaptively tuning variance reduction methods as the simulation progresses. The primary focus of such techniques has been in adaptively tuning importance sampling distributions to reduce the variance of an estimator. We instead focus on adaptive control variate schemes, developing asymptotic theory for the performance of two adaptive control variate estimators. The first estimator is based on a stochastic approximation scheme for identifying the optimal choice of control variate. It is easily implemented, but its performance is sensitive to certain tuning parameters. The second estimator uses a sample average approximation approach. It has the advantage that it does not require any tuning parameters, but it can be computationally expensive and requires the availability of nonlinear optimization software. We present the numerical results of simulation experiments on pricing of barrier options using these two adaptive methods.
(This is joint work with Shane G. Henderson, Cornell University.)
The talk will conclude with a brief overview of ongoing research efforts and future research directions. Traditionally, power system operation and management involves a variety of challenging decision problems, and new interesting problems have emerged with the restructuring of many electricity markets around the globe. In this context, current research on applications of stochastic optimization algorithms for problems such as unit commitment and strategic bidding is described. 
<br><br>
<u>About the Speaker</u>:
Dr. Kim holds a Ph.D. degree in Operations Research from Cornell University. She is currently Visiting Assistant Professor in the Department of Industrial Engineering at Purdue University. Her research interests include simulation methodology, simulation optimization, and applications in electric power system management and air ambulance services.
