<li><b>September 26th, 12:00 p.m., room GB 530</b><br>
<u>Speaker</u>: Ismael de Farias, Department of Industrial Engineering, Texas Tech University<br><br>
<u>Title</u>: Branch-and-Cut without Auxiliary Binary Variables with Applications in Alternative Energy and Data Mining<br><br>
<u>Abstract</u>: 
As a result of the research on polyhedra and cutting planes for mixed-integer programming (MIP) sets developed during the past 20 years, branch-and-cut (B&C) has emerged as a true practical approach for solving difficult combinatorial optimization models arising in real-life applications. Today, it is implemented in all main commercial optimization software, and it has built an impressive record of success on solving to proven optimality industry-strength models with high-end laptop or desktop computers.
<p>
The most important issue in tackling MIP through B&C is how to formulate it. And the most radical discovery on this front is that for a number of extremely large and important classes of models it is best to not introduce integer variables in the formulation. The seed idea here was proposed by Beale and Tomlin in 1970, and further developed by Beale and others. In this talk I will first review Beale's idea of dispensing with the introduction of binary variables to solve MIP efficiently. Then, I will show how to derive cutting planes for these formulations without integer variables. The resulting approach, B&C without auxiliary binary variables, has proven to be considerably more efficient than the "traditional" approach of working with binary variables, despite all recent advances in B&C for integer models. I will present computational results that clearly support this claim, and some recent work on models that contain piecewise linear functions, semi-continuous variables, and cardinality constraints. In the end I will present some exciting opportunities for continued research involving applications in alternative energy and data mining.<br><br>
<u>About the Speaker</u>: Ismael Regis de Farias Jr. obtained his Ph.D. in industrial and systems engineering from the Georgia Institute of Technology in 1995. Previous to that he obtained a M.Sc. in computer science and systems engineering and a B.Sc. in physics, both from the Federal University of Rio de Janeiro. After finishing his Ph.D., he worked for a number of years as a consultant in the IBM Consulting Group and as a faculty in the State University of New York. Currently, he is an Associate Professor in the Department of Industrial Engineering of Texas Tech University. His area of research is operations research, with an emphasis to optimization and integer programming. He is a recent recipient of an IBM Faculty Award and a Second Place Winner of the INFORMS Junior Faculty Interest Group Best Paper Competition.<br><br>

