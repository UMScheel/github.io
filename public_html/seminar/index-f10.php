<li><b>November 5</b><br>
<u>Speaker</u>: Albert Ha, Hong Kong University of Science and Technology<br><br>
<u>Title</u>: Information Sharing in a Supply Chain with a Common Retailer and Production Diseconomies<br><br>
<u>Abstract</u>:
We consider a supply chain with two competing manufacturers selling substitutable products through a common retailer.  Both manufacturers face diseconomies of scale in production.  The retailer decides whether to share a private imperfect demand signal with any of the two manufacturers before observing the signal. A multi-stage game is formulated to analyze how the firms make information sharing, wholesale price and retail price decisions.  We consider several scenarios where the retailer or the manufacturers are the leaders in information sharing decisions and study how such leadership affects the equilibrium outcome. We also perform sensitivity analysis to investigate how different parameters impact the information sharing decisions as well as the performance of the firms.
<br><br>
<u>Speaker's Short Bio</u>:
Albert Y. Ha is Chair Professor and Head of the Department of Information Systems, Business Statistics and Operations Management at the Hong Kong University of Science and Technology (HKUST).  He received his B.Sc. degree in Civil Engineering from the University of Hong Kong, MBA degree from the Chinese University of Hong Kong and Ph.D. degree from the Graduate School of Business of Stanford University.  Prior to joining HKUST in 2001, Professor Ha has served on the faculty of the School of Management of Yale University for nine years.  His research interests are in the areas of Supply Chain Management and Economics of Queuing Systems. He has published in journals such as Management Science, Operations Research, Naval Research Logistics, IIE Transactions and European Journal of Operational Research.  He currently serves as an Associate Editor of Management Science, Operations Research and Manufacturing and Service Operations Management and a Senior Editor of Production and Operations Management.
<br><br>

<li><b>October 22</b><br>
<u>Speaker</u>: Harold Benson, Department of Information Systems and Operations Management, University of Florida<br><br>
<u>Title</u>:
The Concave Cost Supply Management Problem:
Branch and Bound Method with Subproblems
Solvable by Inspection
<br><br>
<u>Abstract</u>:
The concave cost supply management problem (P) is a special global optimization
problem that seeks to minimize a concave cost function over a disconnected nonconvex
set. In this article, we present and validate a branch and bound algorithm for globally
solving problem (P). The algorithm has several advantages. First, the linear programming
subproblems that it generates can be solved essentially by inspection. Second, the algorithm
is guaranteed to either find an exact globally optimal solution for problem (P) or show that
problem (P) is infeasible. Third, the algorithm is finite. Last, the algorithm does not require
that any special conditions hold on the functions or data of problem (P). These properties
are all verified in the article and a sample problem is solved.
<br><br>

<li><b>October 8</b><br>
<u>Speaker</u>: Mac Dada, Carey Business School, Johns Hopkins University<br><br>
<u>Title</u>: A Framework for Supply Chain Coordination<br><br>
<u>Abstract</u>:
This work begins with the analysis of a single link involving a monopolist Supplier, and a single Newsvendor. We focus on a general setting in which each player incurs non-negative economic costs which may include both fixed and variable portions. These values may be direct costs associated with value-adding activities, allocated costs through various accounting schemes, reservation profit levels, or some mixture of the three. Given this structure, we develop a graphical representation to show and explain to the reader/user/manager what constitutes a feasible contract as well as a Pareto-optimal contractual arrangement. We then develop two archetypal contract forms that coordinate this link. We refer to one as a Fixed Tariff Contract, in which the supplier guarantees itself a fixed payoff above its reservation profit level. The other is a Contribution Sharing Contract in which the supplier demands a fixed proportion of the expected contribution above the reservation profit levels. Next we show how several commonly used contract forms are instances or derivatives of these archetypal forms. Following that, we show how an understanding/use of these contract forms provides insights applicable to several current research topics in supply-chain management including cases involving a price-setting Newsvendor, a Newsvendor that affects demand via sales efforts, and contracting with the Newsvendor in the presence of asymmetric demand or cost information. Finally we explain how the application of these archetypal contract forms can be extended over several links forming a serial chain, as well as over a network of chains. We label such networks as Newsvendor Supply Chains. The results provide a useful mental model for characterizing supply-chain interactions.
<br><br>
<u>Speaker's Short Bio</u>:
Maqbool Dada is a Professor of Operations Management at the Johns Hopkins Carey Business School.  His research and teaching include inventory theory, pricing models and service operations.  He received a Ph.D. in Management from MIT and a B.S. in Industrial Engineering and Operations Research from the University of California.
