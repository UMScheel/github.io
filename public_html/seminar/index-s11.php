<li><b>March 11</b><br>
<u>Speaker</u>: Jochen K&ouml;nemann, Department of Combinatorics and Optimization, University of Waterloo, Canada<br><br>
<u>Title</u>: Approximating Steiner Trees via Linear Programming<br><br>
<u>Abstract</u>:
This talk focuses on the classical Steiner tree problem, where one is given a weighted undirected graph G and a vertex subset R of so called terminals. The goal is to find the cheapest tree T that spans R, and may contain other, so called Steiner vertices. This problem has numerous applications, and is well known to be NP-hard to solve exactly. This talk focuses on finding fast approximation algorithms for the problem. Very recently, some very elegant new algorithms have been proposed; these are based on strong linear programming relaxations and random sampling. I will survey these LP relaxations, discuss some of their properties, and describe their algorithmic use. Some of the results presented are based on joint work with David Pritchard, and Deeparnab Chakrabarty.
<br><br>
<u>Speaker's Short Bio</u>:
Jochen K&ouml;nemann spent his undergraduate years in Saarbruecken, Germany, where he studied Computer Science in Kurt Mehlhorn's group at the Max-Planck Institute. He then moved on to Pittsburgh, where he became a student in the graduate program for Algorithms, Combinatorics & Optimization. After a postdoctoral year at University of Rome in Italy, he moved on to the University of Waterloo in Canada, where he is now an Associate Professor and Chair in the Department of Combinatorics & Optimization. Jochen's research focuses broadly on efficient algorithms for NP-hard optimization problems. He is particularly interested in problems arising in the areas of network design and in algorithmic game theory.
<br><br>

<li><b>February 18</b><br>
<u>Speaker</u>: Jonas Baeklund, Department of Mathematical Sciences, University of Aarhus, Denmark<br><br>
<u>Title</u>: Nurse Rostering in a Danish Hospital<br><br>
<u>Abstract</u>:
Nurse rostering is the complex scheduling problem of
planning which shift each nurse should work over a number of consecutive of days.
The problem we confront is a nurse rostering problem in a ward at a Danish
hospital. The problem includes several special regulations that have
to be fulfilled in this ward. This presentation will discuss the first
results of the on-going work of my Ph.D. thesis project. It gives
an overview on the solution approach we are currently working
on. The overall method is a branch-and-price algorithm based on a
linear master problem and a sub-problem handled with heuristics and
constraint programming.
<br><br>

<li><b>January 31</b><br>
<u>Speaker</u>: Yongtao Guan, Division of Biostatistics, Yale University<br><br>
<u>Title</u>: Estimating Individual-Level Risk in Spatial Epidemiology Using Spatially Aggregated Information on the Population at Risk<br><br>
<u>Abstract</u>:
We propose a novel alternative to case-control sampling for the estimation of individual-level risk in spatial epidemiology. Our approach uses weighted estimating equations to estimate regression parameters in the intensity function of an inhomogeneous spatial point process, when information on risk-factors is available at the individual level for cases, but only at a spatially aggregated level for the population at risk. We develop data-driven methods to select the weights used in the estimating equations and show through simulation that the choice of weights can have a major impact on efficiency of estimation. We develop a formal test to detect non-Poisson behavior in the underlying point process and assess the performance of the test using simulations of Poisson and Poisson cluster point processes. We apply our methods to data on the spatial distribution of childhood meningococcal disease cases in Merseyside, U.K. between 1981 and 2007.
