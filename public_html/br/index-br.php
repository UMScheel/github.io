Em alguns casos, t&eacute;cnicas tradicionalmente utilizadas na resolu&ccedil;&atilde;o de modelos de PO e PR,
quando aplicadas isoladamente, n&atilde;o s&atilde;o capazes de resolver os modelos matem&aacute;ticos
