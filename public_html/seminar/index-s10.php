<li><b>April 9</b><br>
<u>Speaker</u>: Sammi Yu Tang, Department of Management, University of Miami<br><br>
<u>Title</u>: Optimal sourcing and subsidy policy when suppliers have random yield loss<br><br>
<u>Abstract</u>:
We consider a decentralized supply chain where a buyer facing deterministic demand sources from supplier(s) whose production process has random yield. The supplier(s) can improve supply reliability by investing in better technology. We first study a traditional contract where the buyer's order quantity provides incentive to the supplier to improve supply reliability. Second, we consider a subsidy contract where the buyer shares the supplier's investment cost of reliability improving effort. Given the contract terms, the supplier(s) then decides on the production size and investment level. We characterize the optimal subsidy policy for the buyer and investigate the effectiveness of order inflation and investment subsidy in dealing with suppliers of varying reliability. We also examine the effect of endogenizing the reliability choice on the buyer's ordering behavior and supplier selection policy.
Joint work with Haresh Gurnani, Diwakar Gupta.
<br><br>

<li><b>March 25</b><br>
<u>Speaker</u>: Yunzeng Wang, Department of Finance and Management Science, University of California, Riverside<br><br>
<u>Title</u>: Supplier Competition in Decentralized Assembly Systems with Price-Sensitive and Uncertain Demand<br><br>
<u>Abstract</u>:
In a decentralized assembly supply chain, independent suppliers produce a set of complementary components from which an assembler assembles a final product and sells it to the market. In such a channel, several competitive forces interact with one another to affect the price and quantity decisions of the firms involved. These include 1) the direct competition each supplier faces for producing the same component, 2) the indirect competition among the suppliers producing the set of complementary components needed for assembling the final product, and 3) the vertical interaction between the assembler and the component suppliers. This paper shows that the direct competition that one supplier faces helps improve the performance of the assembler and all the other suppliers in the channel; and surprisingly, it can help improve the performance of this particular supplier facing the competition as well. Second, the assembler benefits from a merger of suppliers producing different components in the complementary set; furthermore, the assembler prefers a merger of suppliers with less direct competition over a merger of suppliers with more direct competition.
<br><br>
<u>About the Speaker</u>:
Yunzeng Wang is the Dean's Distinguished Scholar in Supply Chain Management and Professor of Finance and Management Science at the A. Gary Anderson School of Management, University of California, Riverside. He holds a Ph.D. degree in Operations Management from the Wharton School, University of Pennsylvania.  Previously, Dr. Wang served as faculty member at the Weatherhead School of Management, Case Western Reserve University, and at the School of Management, University of Texas at Dallas. Dr. Wang's research interests focus on supply chain management.  His publications have appeared in academic journals including Management Science, Operations Research, Manufacturing and Service Operations Management, Production and Operations Management, Naval Logistics Research, and European Journal of Operational Research, and IIE Transactions.
<br><br>

<li><b>February 25</b><br>
<u>Speaker</u>: Suvrajeet Sen, Department of Integrated Systems Engineering, Ohio State University<br><br>
<u>Title</u>: Risk, Recourse, and Resilience via Stochastic Combinatorial Optimization<br><br>
<u>Abstract</u>:
The stochastic programming literature has been mainly devoted to continuous stochastic optimization models in which discrete decisions are not allowed. However, there are numerous applications, especially those arising in the context of decision-making for homeland security problems, in which discrete choices are natural. In this talk, I will present several applications for which the use of stochastic combinatorial optimization (SCO) is absolutely essential. Yet, it is only within the last 10 years during which researchers have begun to investigate methods for solving SCO problems without brute-force enumeration. In this lecture, I will begin by discussing three applications. One application arises from the need to model the Kahneman/Tversky "S-curve". Another application deals with a stochastic "server" location problem in which servers are to be located before demand seems firm. And a final application will be devoted to network design under threat. These applications lead to models that are so large, that even the most powerful MIP solvers are unable to provide a feasible solution. After motivating the need for new methodology to handle such problems, we will discuss some algorithmic strategies that are the only ones to be able to solve these very large scale optimization models. On the other hand, advances in decomposition-coordination methods have made it possible to solve these very large scale models. It is worth noting that the most well known deterministic solvers are unable to solve most of the challenging instances. The key insights leading to the SCO decomposition rely on effective use of successive convexification. We will summarize a collection of algorithms that have been developed for the solution of such problems, with the largest of these containing over a million binary variables!
<br><br>

<li><b>January 29</b><br>
<u>Speaker</u>: Tallys Yunes, Department of Management Science, University of Miami<br><br>
<u>Title</u>: A Polyhedral Study of the Cumulative Constraint<br><br>
<u>Abstract</u>:
The cumulative constraint is used to describe scheduling problems in which a machine can process more than one job at a time. In addition to pure job scheduling, this constraint has applications in other areas, such as resource-constrained project scheduling and berth allocation at ports.
We study the convex hull of feasible solutions to the cumulative constraint and propose families of facet-defining inequalities for the case in which all jobs have identical data (release and due dates, processing time, and resource consumption). Those families can be used to derive valid inequalities for problems with non-identical job data. We also propose a family of facet-defining inequalities that are valid for jobs that differ only with respect to resource consumption. We present some very preliminary computational results obtained with the implementation of our cuts inside a branch-and-cut algorithm for single-machine cumulative scheduling.
This is joint work in progress with Dimitrios Magos and Ioannis Mourtos.
<br><br>

