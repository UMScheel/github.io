<h2>Past Talks</h2>
<p>
<ul>
<li><b>December 14th</b><br>
<u>Speaker</u>: Ling Wang, Management Department, University of Miami<br><br>
<u>Title</u>: Joint Price and Due-Date Quotation: Monopolistic and Competitive Cases<br><br>
<u>Abstract</u>:
We consider a joint due-date and price quotation problem in the make-to-order setting under both 
monopolistic and competitive environments. In the monopolistic model a single firm quotes price and 
due-date to incoming customers. Customers either accept or reject the joint quote according to a 
probabilistic acceptance function.  We show the firm's optimal policy is to promise the earliest available 
due-date and offer a price uniquely determined by its current backlog size. In the competitive model 
two firms compete to win customers by offering a joint quote better than the rival. We provide 
conditions for the existence of Nash equilibria and characterize due-dates and prices at equilibrium. 
We identify and explain a seemingly counter-intuitive behavior - "lose-for-now" - whereby the firm 
with smaller backlog attempts to lose the current customer to the rival. We conduct numerical studies 
to examine the impact of system parameters on prices and expected profits at equilibrium. We 
identify two factors that dampen the competition and lead to high prices: backlog imbalance between 
firms and high utilization.
<br><br>

<p>

<li><b>December 4th</b><br>
<u>Speaker</u>: Hernan Awad, Management Science Department, University of Miami<br><br>
<u>Title</u>: Importance Sampling for Markov Process Expectations<br><br>
<u>Abstract</u>:
We study the use of importance sampling (IS) to estimate a class of
expectations of functionals of Markov processes. These expectations
are more general than rare event probabilities, and include the
expected cumulative discounted reward until hitting a set as a
particular case. Experience in the rare-event simulation context
shows that characterizing the form of the zero-variance importance
distribution can often help in designing an efficient importance
sampling estimator. For more general expectations in the Markov
setting, however, the zero-variance importance distribution often
destroys the Markovian nature of the underlying process, making it
difficult to simulate. For the class of expectations we consider, we
show that a zero-variance estimator can be constructed by using an
importance distribution that preserves the Markovian nature of the
underlying process. The importance distribution depends on the
solution of a linear system which characterizes the desired
expectation. This suggests that, if an approximate solution to the
linear system is available, it can be used to construct a good
practical importance sampling distribution that  preserves the
Markovian nature of the underlying process.
<p>
The first part of the talk will consist of a brief primer on
importance sampling techniques.
