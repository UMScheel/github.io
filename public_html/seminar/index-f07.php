<li><b>November 14th</b><br>
<u>Speaker</u>: John Liechty, Smeal College of Business, Penn State University<br><br>
<u>Title</u>: Attribute Level Heterogeneity: Improving Model Based Clustering by Aggregating Information Along Each Dimension<br><br>
<u>Abstract</u>: 
In the world of marketing, clustering consumer preferences (commonly referred to as consumer 
heterogeneity) has played an important role in understanding market structure and allowing 
practitioners to engage in group and individual level marketing actions.  In practice, this clustering has 
been done using finite mixture models which offers a tool for gaining stylized insights into market 
segments.  Finite mixture models typically cluster 'partworth' utility vectors in P dimensional space - 
assuming that all of the elements of the partworth vector parameters are the same for each mixture 
component, a vector finite mixture model.  We propose and explore an alternate finite mixture 
specification, where each element of the partworth vector has its own finite mixture, an attribute 
finite mixture model.  We address a number of statistical issues, such as contrasting the alternate 
specification with the traditional model and the computational challenges associated with model 
choice for the attribute mixture model.  In addition, we demonstrate that by aggregating information 
along each of the dimensions of the partworth vector, the attribute model is able to identify a complex 
structures with less data, when compared to the vector model; leading to a robust approach to 
segmentation which results in complex market structure that is readily accessible to managers.  
<br><br>

